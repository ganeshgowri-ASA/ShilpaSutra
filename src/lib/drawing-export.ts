export function exportBOMAsCSV(
  parts: { name: string; quantity: number; material: string; weight: number }[],
  filename: string,
): void {
  const header = "Item #,Part Name,Quantity,Material,Weight (kg)\n";
  const rows = parts
    .map((p, i) => `${i + 1},"${p.name}",${p.quantity},"${p.material}",${p.weight.toFixed(3)}`)
    .join("\n");
  const totalWeight = parts.reduce((sum, p) => sum + p.weight * p.quantity, 0);
  const footer = `\n,TOTAL,${parts.reduce((s, p) => s + p.quantity, 0)},,${totalWeight.toFixed(3)}`;

  const blob = new Blob([header + rows + footer], { type: "text/csv" });
  const link = document.createElement("a");
  link.download = `${filename}.csv`;
  link.href = URL.createObjectURL(blob);
  link.click();
  URL.revokeObjectURL(link.href);
}

export function exportBOMAsPDF(
  parts: { name: string; quantity: number; material: string; weight: number }[],
  assemblyName: string,
  filename: string,
): void {
  let content = `BILL OF MATERIALS\n`;
  content += `Assembly: ${assemblyName}\n`;
  content += `Date: ${new Date().toISOString().split("T")[0]}\n`;
  content += `${"=".repeat(70)}\n\n`;
  content += `${"Item".padEnd(6)}${"Part Name".padEnd(25)}${"Qty".padEnd(6)}${"Material".padEnd(20)}${"Weight(kg)".padStart(12)}\n`;
  content += `${"-".repeat(70)}\n`;

  parts.forEach((p, i) => {
    content += `${String(i + 1).padEnd(6)}${p.name.padEnd(25)}${String(p.quantity).padEnd(6)}${p.material.padEnd(20)}${p.weight.toFixed(3).padStart(12)}\n`;
  });

  const totalWeight = parts.reduce((sum, p) => sum + p.weight * p.quantity, 0);
  content += `${"-".repeat(70)}\n`;
  content += `${"".padEnd(6)}${"TOTAL".padEnd(25)}${String(parts.reduce((s, p) => s + p.quantity, 0)).padEnd(6)}${"".padEnd(20)}${totalWeight.toFixed(3).padStart(12)}\n`;
  content += `\nGenerated by ShilpaSutra CAD Platform\n`;

  const blob = new Blob([content], { type: "application/pdf" });
  const link = document.createElement("a");
  link.download = `${filename}.pdf`;
  link.href = URL.createObjectURL(blob);
  link.click();
  URL.revokeObjectURL(link.href);
}
