export interface DXFEntity {
  type: "LINE" | "CIRCLE" | "ARC" | "TEXT" | "POLYLINE" | "DIMENSION";
  layer: string;
  data: Record<string, number | string>;
}

export function generateDXFContent(entities: DXFEntity[], title: string): string {
  let dxf = "";

  // Header section
  dxf += "0\nSECTION\n2\nHEADER\n";
  dxf += "9\n$ACADVER\n1\nAC1027\n";
  dxf += "9\n$INSUNITS\n70\n4\n"; // millimeters
  dxf += "0\nENDSEC\n";

  // Tables section
  dxf += "0\nSECTION\n2\nTABLES\n";
  // Layer table
  dxf += "0\nTABLE\n2\nLAYER\n70\n6\n";
  const layers = ["0", "VISIBLE", "HIDDEN", "CENTER", "DIMENSION", "TEXT", "HATCH", "GDT"];
  const layerColors: Record<string, number> = {
    "0": 7, VISIBLE: 7, HIDDEN: 3, CENTER: 1, DIMENSION: 5, TEXT: 7, HATCH: 6, GDT: 4,
  };
  layers.forEach((layer) => {
    dxf += `0\nLAYER\n2\n${layer}\n70\n0\n62\n${layerColors[layer] || 7}\n6\nCONTINUOUS\n`;
  });
  dxf += "0\nENDTAB\n";
  dxf += "0\nENDSEC\n";

  // Entities section
  dxf += "0\nSECTION\n2\nENTITIES\n";
  entities.forEach((entity) => {
    switch (entity.type) {
      case "LINE":
        dxf += `0\nLINE\n8\n${entity.layer}\n`;
        dxf += `10\n${entity.data.x1}\n20\n${entity.data.y1}\n30\n0\n`;
        dxf += `11\n${entity.data.x2}\n21\n${entity.data.y2}\n31\n0\n`;
        break;
      case "CIRCLE":
        dxf += `0\nCIRCLE\n8\n${entity.layer}\n`;
        dxf += `10\n${entity.data.cx}\n20\n${entity.data.cy}\n30\n0\n`;
        dxf += `40\n${entity.data.r}\n`;
        break;
      case "TEXT":
        dxf += `0\nTEXT\n8\n${entity.layer}\n`;
        dxf += `10\n${entity.data.x}\n20\n${entity.data.y}\n30\n0\n`;
        dxf += `40\n${entity.data.height || 2.5}\n`;
        dxf += `1\n${entity.data.text}\n`;
        break;
      case "ARC":
        dxf += `0\nARC\n8\n${entity.layer}\n`;
        dxf += `10\n${entity.data.cx}\n20\n${entity.data.cy}\n30\n0\n`;
        dxf += `40\n${entity.data.r}\n`;
        dxf += `50\n${entity.data.startAngle}\n51\n${entity.data.endAngle}\n`;
        break;
    }
  });
  dxf += "0\nENDSEC\n";
  dxf += "0\nEOF\n";

  return dxf;
}

export function exportDXF(entities: DXFEntity[], filename: string): void {
  const content = generateDXFContent(entities, filename);
  const blob = new Blob([content], { type: "application/dxf" });
  const link = document.createElement("a");
  link.download = `${filename}.dxf`;
  link.href = URL.createObjectURL(blob);
  link.click();
  URL.revokeObjectURL(link.href);
}

